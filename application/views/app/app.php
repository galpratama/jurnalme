              <ol>
					<li><strong>Olahraga</strong><br />Olahraga pagi itu penting. Lakukan baik di&nbsp;<em>gym</em>&nbsp;atau bentuk olahraga lain. Olahraga pagi selain penting untuk persiapan kerja sepanjang hari juga memperkecil kemungkinan tertimbunnya flek pada jantung.</li>
					<li><strong>Petakan rencana kerja hari ini.<br /></strong>Ini dilakukan untuk mengoptimalkan potensi yang dimiliki. Dengan membuat jadwal yang akan dikerjakan sepanjang hari ini semua pekerjaan akan bisa dilakukan dengan baik sesuai prioritas. Kebiasaan ini seperti sarapan. Jika sarapan pagi kita baik maka sepanjang hari akan baik. Jika persiapan kita baik maka pekerjaan kita pun akan baik.</li>
					<li><strong>Sarapan pagi yang sehat.<br />&nbsp;</strong>Sudah banyak yang menganjurkan sebelum bekerja sebaiknya sarapan. Banyak yang melakukan sarapan pagi di kantor karena kesibukan atau terburu-buru karena bangun agak siang. Tetapi akan lebih baik jika itu dilakukan di rumah. Selain lebih sehat juga tetap menjaga kedekatan dengan keluarga.</li>
					<li><strong>Visualisasi.<br /></strong>Bayangkan sukses apa yang akan diraih hari ini. Membuat visualisasi keberhasilan yang mau diraih hari ini akan membantu menjaga&nbsp;<em>mood</em>&nbsp;sepanjang hari. Meski hanya memvisualisasikannya barang semenit, itu akan berpengaruh.</li>
					<li><strong>Jadikan hari Anda padat.</strong> <br />Setiap dari kita pasti punya daftar pekerjaan yang menakutkan. Kadang selalu ditunda untuk melakukannya sehingga akhirnya banyak waktu terbuang percuma dan membuat stres. Untuk mengatasinya coba masukkan pekerjaan terberat itu untuk dilakukan sepagi mungkin atau setidaknya menjelang makan siang. Hal ini penting karena di pagi hari kondisi tubuh lebih segar dan tenaga sedang penuh. Dengan demikian masalah berat itu akan lebih mudah diatasi.&nbsp;</li>
